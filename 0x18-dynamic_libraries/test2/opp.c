/**
 * add - add two numbers
 * @a: the first number
 * @b:the second Number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sum - subtract two number
 * @a: the first number
 * @b: the second number
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication of two numbers
 * @a: the first number
 * @b: the second Number
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mul - division of two numbers
 * @a: the first number
 * @b: the second Number
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modules of two numbers
 * @a: the first number
 * @b: the second Number
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
